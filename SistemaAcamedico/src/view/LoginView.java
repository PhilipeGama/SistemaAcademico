    private void jbtEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtEnviarActionPerformed
        // TODO add your handling code here:
        try {
            AdministradorDAO dao = new AdministradorDAO();
            listaAdmn = dao.consultar("");
        } catch (Exception e) {
        }
        String login = jtfLogin.getText();
        String senha = jtfSenha.getText();
        boolean ver = true;
        if(login.isEmpty() || senha.isEmpty()){
            JOptionPane.showMessageDialog(rootPane,"Todos os campos obrigatoris");
            ver = false;
        }
        if(ver){
          int i =0;
          while(i <listaAdmn.size()){
          Administrador adm = listaAdmn.get(i);
          if(login.equals(adm.getLogin()) && senha.equals(adm.getSenha())){
            InicialView telaInicial = new InicialView();
            telaInicial.setVisible(true);
            this.dispose();
            ver = false;
          }
          i++;
          }
           
        }
        if(ver = false){
           JOptionPane.showMessageDialog(null,"Senha ou Login Incorreto");
        }
         
        
    }//GEN-LAST:event_jbtEnviarActionPerformed

